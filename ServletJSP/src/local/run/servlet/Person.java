package local.run.servlet;

public class Person {
	
	public Person() {
		
	}
	
	private String imie;
	private String nazwisko;
	private String email;
	private String pracodawca;
	private String skadWiem;
	private String inneJakie;
	private String zajecie;
	
	public String getImie() {
		return imie;
	}
	public void setImie(String imie) {
		this.imie = imie;
	}
	public String getNazwisko() {
		return nazwisko;
	}
	public void setNazwisko(String nazwisko) {
		this.nazwisko = nazwisko;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPracodawca() {
		return pracodawca;
	}
	public void setPracodawca(String pracodawca) {
		this.pracodawca = pracodawca;
	}
	public String getSkadWiem() {
		return skadWiem;
	}
	public void setSkadWiem(String skadWiem) {
		this.skadWiem = skadWiem;
	}
	public String getInneJakie() {
		return inneJakie;
	}
	public void setInneJakie(String inneJakie) {
		this.inneJakie = inneJakie;
	}
	public String getZajecie() {
		return zajecie;
	}
	public void setZajecie(String zajecie) {
		this.zajecie = zajecie;
	}
	
	
}
